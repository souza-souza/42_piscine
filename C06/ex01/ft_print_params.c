#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	p;

	i = 1;
	while (i < argc)
	{
		p = 0;
		while (argv[i][p])
		{
			p++;
		}
		write (1, argv[i], p);
		write (1, "\n", 1);
		i++;
	}
	return (0);
}
