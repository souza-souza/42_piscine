#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	c;

	i = argc - 1;
	while (i > 0)
	{
		c = 0;
		while (argv[i][c])
		{
			c++;
		}
		write (1, argv[i], c);
		write (1, "\n", 1);
		i--;
	}
	return (0);
}
